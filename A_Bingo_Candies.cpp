void solve()
{
    int n;
    cin >> n;
    if (n == 1)
    {
        cout << "NO" << endl;
        return;
    }
    unordered_map<int, int> mp;
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < n; j++)
        {
            int x;
            cin >> x;
            mp[x]++;
        }
    }
    for (auto it : mp)
    {
        if (it.second > n * n - n)
        {
            cout << "NO" << endl;
            return;
        }
    }
    cout << "YES" << endl;
}

