void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];

    int ans = 0;
    for (int i = 1; i < n; i++)
    {
        if (a[i] + a[i - 1] == 7 || a[i] == a[i - 1]){
            ans++;
            i++;
        }
    }
    cout << ans << endl;
}

